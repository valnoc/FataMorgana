import Foundation

func anyString() -> String {
    return ""
}

func anyStringArray() -> [String] {
    return ["123", "qwe"]
}

func anyIntArray() -> [Int] {
    return [0, 1, 2]
}

func anyStringStringDict() -> [String: String] {
    return ["123": "qwe"]
}

func anyStringIntDict() -> [String: Int] {
    return ["123": 1]
}
